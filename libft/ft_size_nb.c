#include "libft.h"

int	ft_size_nb(int n)
{
	int index;

	index = 0;
	if (n < 0)
		index++;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n = n / 10;
		index++;
	}
	return (index);
}
